<h3>Chapter 13: File Permisssions</h3>
<hr />


<p><strong>Quiz</strong></p> 
<ul>
    <li>Which permission is necessary on a directory in order for a user to use the cd command to change that directory?: Execute</li>
    <li>Which permission, when set on a directory, causes new files and directories that are created inside to be owned by the group that owns the directory?: <code>setgid</code></li>
    <li>To run binary executables, you need both read and execute permission.: False </li>
    <li>Which command will allow you to change the group ownership of a file that you own?: <code>chgrp</code></li>
    <li>The group ownership of a file may only be changed by: Both the root user and the owner of the file</li>
    <li>For rwxr-x--x permission set, the bolded permissions belong to: All users besides the user owner and group owner</li>
    <li>What umask value would you use in order for new directories to have the permissions of rwxr-x--x?: 026</li>
    <li>Which commands can be used to change the ownership of a file? </li>
    <li>(choose two): <code>chown</code>, <code>chgrp</code></li>
    <li>The only user with the capability to change the owner of a file is root: True</li>
    <li>To access a directory with the cd command, you must also be able to access all the parent directories of the directory with the cd command.: True</li>
    <li>To run a script file, you need both read and execute permission: True</li>
    <li>Assuming everyone has access to the directory the file is in, who can view the contents of a file with permissions of rw-r----x?: Both the user owner and group owners</li>
    <li>To switch to another group, you must: Be a member of the group that you are switching to</li>
    <li>For the rwxr-x--x permission set, the bolded permissions belong to: The group owner of the file</li>
    <li>To have permissions set to rwxr-x--x, you can use the following command: <code>chmod 751</code></li>
    <li>The command, chmod 1777 /data will: Make the /data directory a sticky bit directory</li>
    <li>Which command will allow you to switch to another group and add files that are group owned by this other group?: <code>newgrp</code></li>
    <li>Which command is used to change permissions on a file?: <code>chmod</code></li>
    <li>The command, chmod a+x will: Add execute permission for all users</li>
    <li>The command, chmod u=g will: Set the user owners permissions to match the group owners permission</li>
    <li>The command, chmod 0777 /data will: Remove all special permissions</li>

</ul>